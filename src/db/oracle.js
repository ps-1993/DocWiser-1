export async function getTemplate(templateId) {
  return withConnection(async (connection) => {
    const result = await connection.execute(
      `SELECT
         id,
         original_name,
         stored_name,
         storage_path,
         mime_type,
         file_size,
         template_text,
         rules_json,
         created_at
       FROM templates
       WHERE id = :id`,
      {
        id: {
          val: Number(templateId),
          type: oracledb.NUMBER
        }
      }
    );

    return result.rows[0] || null;
  });
}

